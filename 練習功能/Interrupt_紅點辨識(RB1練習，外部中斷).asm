list p=18f4520
#include <p18f4520.inc>
CONFIG OSC=HS, PWRT=ON, WDT=OFF, LVP=OFF, PBADEN=OFF

org  0x00
GOTO  INITIAL

org  0x08
BCF INTCON3,INT1IF
GOTO  JUDGE_1

org  0x40
INITIAL:
  CALL INITIAL_2
  CLRF TRISD
  BCF TRISB,1
  BSF TRISA,4
  BCF LATB,1
  GOTO MAIN

MAIN:
  CLRF PORTD
  BTFSS PORTA,4
  BSF LATB,1
  GOTO MAIN

INITIAL_2:  
  BSF RCON,IPEN
  BSF INTCON,GIEH
  BSF INTCON,GIEL
  BCF INTCON3,INT1IF

  BSF INTCON2,INTEDG1
  BSF INTCON3,INT1IP
  BSF INTCON3,INT1IE
  RETURN
JUDGE_1:
  MOVLW B'01010101'
  MOVWF PORTD
  BTFSS PORTA,4
  GOTO JUDGE_1
  BCF LATB,1
  RETFIE
END  





