list p=18f4520
#include <p18f4520.inc>
CONFIG OSC=HS, PWRT=ON, WDT=OFF, LVP=OFF, PBADEN=OFF
COUNT_TWO equ (.256-.2)

org 0x00
GOTO INITIAL

org 0x08
   SWAPF PORTD
   MOVLW COUNT_TWO
   MOVWF TMR0L
   BCF INTCON, TMR0IF
   RETFIE

org 0x2A
INITIAL:
   BSF TRISA,4
   CLRF TRISD
   MOVLW B'11100000'
   MOVWF PORTD
   MOVLW B'11101000'
   MOVWF T0CON
   CLRF TMR0L
   MOVLW COUNT_TWO
   MOVWF TMR0L

MAIN:
   GOTO MAIN

END

